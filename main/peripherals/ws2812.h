
#ifndef WS2812_H
#define WS2812_H


/********************************************************************************************
 *                              INCLUDES
 ********************************************************************************************/
 #include <stdint.h>

 /********************************************************************************************
 *                              DEFINES
 ********************************************************************************************/

/********************************************************************************************
 *                              TYPEDEFS
 ********************************************************************************************/
typedef union {
  struct __attribute__ ((packed)) 
  {
    uint8_t r, g, b;
  };
  uint32_t num;
} rgbVal;

/********************************************************************************************
 *                           GLOBAL VARIABLES
 ********************************************************************************************/
 
 /********************************************************************************************
 *                           STATIC VARIABLES
 ********************************************************************************************/
 
 /********************************************************************************************
 *                           STATIC PROTOTYPE
 ********************************************************************************************/
 
 /********************************************************************************************
 *                           STATIC FUNCTIONS
 ********************************************************************************************/

/********************************************************************************************
 *                           GLOBAL FUNCTIONS
 ********************************************************************************************/
void ws2812_init(int gpioNum);
void ws2812_setColors(unsigned int length, rgbVal *array);

inline rgbVal makeRGBVal(uint8_t r, uint8_t g, uint8_t b)
{
  rgbVal v;


  v.r = r;
  v.g = g;
  v.b = b;
  return v;
}

#endif /* WS2812_H */
